#include <stdio.h>
/**
 * main- entry point for thr program.
 * char- first letter of alphabet
 * while- function for code
 * Return:0 if no errors, return non-zeros if errors
 */
int main(void)
{
	char start = 'a';
	char end = 'z';

	char a = start;
	while (a != end)


	{
		putchar("a\n", a);
		a++;
	}
	return (0);
}
